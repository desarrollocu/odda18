    private void cargarEtiqueta(Trabajador t) {
        this.lblArea.setText(t.getSectorTrabajador().getArea().getNomArea());
        this.lblSector.setText(t.getSectorTrabajador().getNomSector());
        this.lblTrabajador.setText(t.getNombreCompleto().toUpperCase());
    }

    @Override
    public void actualizarPuestos(List<Puesto> puestos) {
        lstPuestos.setListData(puestos.toArray());
    }

    private void asignarTrabajadorAPuesto() {
        Puesto miPuesto = (Puesto) lstPuestos.getSelectedValue();
        controlador.asignarTrabajadorAPuesto(miPuesto, trabajador);
    }

    @Override
    public void abrirAtencionTrabajador(Trabajador t) {
        dispose();
        new AtencionTrabajador(t).setVisible(true);
    }
